package zadatak9;

public class Vehicle {

	private String color, New;
	private int x, y;



	public String getNew() {
		return New;
	}

	public void setNew(String red) {
		this.New = red;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}



}
